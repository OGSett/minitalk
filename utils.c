#include "minitalk.h"

int	compute_num(const char *str)
{
	int	n;
	int	i;

	n = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] > '9' || str[i] < '0')
			return (-1);
		else
		{
			n = n * 10 + (str[i] - '0');
		}
		i++;
	}
	return (n);
}
